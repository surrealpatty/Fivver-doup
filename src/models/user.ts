// src/models/user.ts
import { DataTypes, Model } from 'sequelize';
import { sequelize } from '@config/database';  // Adjust this import based on your project structure

interface UserAttributes {
  id: string;  // The id will be auto-generated by Sequelize (UUID)
  email: string;
  username: string;
  password: string;
  role: string;
  tier: string;
  isVerified: boolean;
}

export class User extends Model<UserAttributes> implements UserAttributes {
  public id!: string;  // The 'id' field will be auto-generated by Sequelize
  public email!: string;
  public username!: string;
  public password!: string;
  public role!: string;
  public tier!: string;
  public isVerified!: boolean;
}

User.init(
  {
    id: {
      type: DataTypes.UUID,
      defaultValue: DataTypes.UUIDV4,  // Auto-generate UUID
      primaryKey: true,
    },
    email: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
    },
    username: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    password: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    role: {
      type: DataTypes.STRING,
      allowNull: false,
      defaultValue: 'free',
    },
    tier: {
      type: DataTypes.STRING,
      allowNull: false,
      defaultValue: 'free',
    },
    isVerified: {
      type: DataTypes.BOOLEAN,
      allowNull: false,
      defaultValue: false,
    },
  },
  {
    sequelize,
    modelName: 'User',
  }
);
